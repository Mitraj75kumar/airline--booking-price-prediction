import numpy as np

# Create an array from 0 to 9
arr_range1 = np.arange(10)
print("Array from 0 to 9:")
print(arr_range1)

# Create an array from 2 to 10 with a step of 2
arr_range2 = np.arange(2, 11, 2)
print("\nArray from 2 to 10 with step 2:")
print(arr_range2)

# Create an array with floating-point steps
arr_range3 = np.arange(0, 2.5, 0.5)
print("\nArray with floating-point step:")
print(arr_range3)