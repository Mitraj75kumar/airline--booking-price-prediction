Marks = int(input("Enter a Marks :"))

if Marks>=81 and Marks <=100:
    print(f"{Marks} marks is very good. ")

elif Marks>= 61 and Marks<=80:
    print(f"{Marks} marks is good. ")    
elif Marks>=41 and Marks<=60:
    print(f"{Marks} marks is average.")
else:
    print(f"{Marks} marks is fail.")        